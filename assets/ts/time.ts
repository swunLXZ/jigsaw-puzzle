const {ccclass, property} = cc._decorator;

@ccclass
export default class NewClass extends cc.Component {

    @property(cc.Label)
    label: cc.Label = null;

    @property
    text: string = 'hello';

     time :number = 59;

    // LIFE-CYCLE CALLBACKS:

    // onLoad () {}

    start () {
        this.schedule(function(){
            this.node.getComponent(cc.Label).string = "剩余时间 : "+this.time+"秒";
            this.time--;
        },1)

    }

    // update (dt) {}
}
